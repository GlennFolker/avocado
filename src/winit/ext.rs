use crate::incl::*;

pub trait ColorExt: Into<wgpu::Color> {}
impl ColorExt for Color {}

impl Into<wgpu::Color> for Color {
    fn into(self) -> wgpu::Color {
        wgpu::Color {
            r: self.r as f64,
            g: self.g as f64,
            b: self.b as f64,
            a: self.a as f64,
        }
    }
}
